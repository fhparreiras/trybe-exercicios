//Exercício 1 - Cinco programas, um para cada operação aritmética básica

const a = 4;
const b = 13;

//Adição
let sum = a + b;
console.log("Resultado Adição: " + sum);

//Subtração
let subtraction = a - b;
console.log("Resultado Subtração: " + subtraction);

//Multiplicação
let mult = a * b;
console.log("Resultado Multiplicação: " + mult);

//Divisão
let divisao = a / b;
console.log("Resultado Divisão: " + divisao);

//Módulo
let modulo = a % b;
console.log("Resultado Módulo: " + modulo);
